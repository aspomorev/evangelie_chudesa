                    <div class="w-64 float-right">
                        <p>Творчество Николая Николаевича Ге, русского рисовальщика и живописца, составляют портреты и работы на историческую и религиозную тематику, отличающиеся глубокой психологической проработкой, цветовым драматизмом и тонкой игрой света и тени.&nbsp; Он оказал значительное влияние на развитие реалистичной живописи, а также известен как один из главных учредителей Товарищества передвижных художественных выставок.</p>
                        <p><strong>Известные картины:</strong> &laquo;Тайная вечеря&raquo; (1863), &laquo;Христос в Гефсиманском саду&raquo; (1869), &laquo;Пётр I допрашивает царевича Алексея Петровича в Петергофе&raquo; (1872), &laquo;Портрет Льва Николаевича Толстого&raquo; (1884), &laquo;Христос с учениками входит в Гефсиманский сад&raquo; (1888), &laquo;Совесть. Иуда&raquo; (1891).</p>
                        <p>Н.Н. Ге родился в г. Воронеже в 1831 в семье французского происхождения, чьи предки эмигрировали во время Французской революции. Рано потеряв мать, умершей от холеры, Николай Ге рос вместе с братьями на попечительстве бабушки и няни.</p>
                        <p>По наставлению отца поступил на математическое отделение в Императорский Университет Святого Владимира в г. Киеве. Рисованием увлекался с детства. Во время обучения самостоятельно изучал живопись, активно интересовался искусством &ndash; в особенности работами Карла Брюллова, чья живописная манера впоследствии оказала влияние на собственный стиль художника.</p>
                        <p>Проучившись год, переезжает к брату в Санкт-Петербург, где вначале продолжает обучение на математическом факультете. Однако занятия живописью не оставляет и в итоге, как говорил сам художник, &laquo;искусство перетянуло&raquo;<a href="#_ftn1" name="_ftnref1">[1]</a>. В 1850 году бросает университет и поступает в Академию художеств, куда во многом стремился за работами кумира &ndash; Карла Брюллова, чья &laquo;Помпея&raquo; до сих пор восхищает множество людей. Однако пройти обучение у великого мастера не удалось по причине его плохого здоровья и вскоре смерти, учителем Николая Ге был Пётр Васильевич Басин, автор религиозных и исторический работ, известный художник-монументалист.&nbsp;</p>
                        <p>Из академии Н.Н. Ге выпускается с золотой медалью, что дало ему возможность получить пенсион, &ndash; денежное пособие на совершение заграничной поездки &ndash; которого удостаивались лишь лучшие. Отправился живописец вместе с супругой Анной Ге в Италию, где смог в Риме увидеть и оценить работу художника Александра Иванова, который тогда завершил &laquo;Явление Христа народу&raquo;. Под ее влиянием Николай Ге обратился к религиозным мотивам. Из пенсионерской поездки он должен был отправлять отчет о своем обучении и знакомстве с мастерами прошлого, а по завершении пенсии предоставить итоговую работу, которая бы воплотила все полученные новые навыки и знания.</p>
                        <p>На суд Академии художник в 1863 году привозит &laquo;Тайную вечерю&raquo;, за чье искусное исполнение ему присудили звание профессора исторической живописи. В Петербург Н.Н. Ге с семьей возвращается лишь к 1870 году и становится учредителем Товарищества передвижных выставок, в котором принимает активное участие. Для проведения первой выставки художник пишет работу &laquo;Пётр I допрашивает царевича Алексея Петровича в Петергофе&raquo;. Во время петербургского периода создает множество портретов (из известных И. Тургенева, Н. Некрасова, М. Салтыкова-Щедрина, Л. Толстого) и исторических картин на отечественные сюжеты (например, &laquo;Екатерина II у гроба Елизаветы&raquo;), которые были бы близки и понятны народу, что являлось одной из главной целью Товарищества.</p>
                        <p>Остаток жизни Н.Н. Ге провел на хуторе в Черниговской губернии, где продолжил работать над религиозными сюжетами, а за год до смерти в 1894 году он создал автопортрет, вставший в ряд его лучших работ.</p>
