            <h2>О проекте «Евангелие. Образы и образа»</h2>
            <div class="content-full op3">
                <div class="content-full-text">

                    <div class="content-full-box o-project">
                        <p>Духовно-просветительский многосерийный проект &laquo;Евангелие. Образы и образа&raquo; представляет собой интерактивный электронный контент в формате мультимедийной платформы, посвященный изучению Священного писания сквозь призму отечественного искусства.</p>
                        
                        <p>Актуальность проекта коррелируется с актуальностью проблемы духовного развития и всестороннего гармоничного образования, обусловленной важнейшими для современного российского общества задачами сохранения национального наследия и духовных традиций отечественной культуры.</p>
                        
                        <p>В одном из посланий Президента Российской Федерации В. В. Путина к Федеральному Собранию, оценивая состояние дел в духовно-нравственной сфере как &laquo;демографическую и ценностную катастрофу&raquo;, Президент поставил задачу &laquo;укреплять прочную духовно-нравственную основу общества&raquo;. Глава государства сформулировал стандарт воспитанности молодого человека: &laquo;Формирование нравственного, гармоничного человека, ответственного гражданина России&raquo;.</p>
                        
                        <p>В качестве одной из существенных причин наблюдающихся деструктивных процессов правомерно назвать нравственно-этическую и духовно-религиозную непросвещенность значительной части социума, что позволяет сделать обоснованный вывод о необходимости реализации на всех уровнях отечественной образовательной системы долгосрочных просветительских проектов, направленных на приобщение учащейся молодежи к основам духовно-нравственной и культурно-исторической традиции.</p>
                        
                        <p>&laquo;В современных условиях углубления процесса глобализации образования и культуры для свободной социальной ориентации в информационном потоке человек должен обладать информационной культурой как одной из составляющих общей культуры. Информационная культура неразрывно связана с социальной природой человека. И разобраться в огромнейшем потоке всей информации невозможно вследствие отсутствия внутреннего морального стержня, устойчивых ценностных ориентиров, внутренней стабильности. Решить эту проблему можно только с позиций современного научного знания. Это знание по определению &ndash; гуманитарное&raquo;. [2, с. 39-40]</p>
                        
                        <p>Мы полагаем, что полноценное культурологическое, гуманитарное образование, тем более высшее, не может осуществляться без духовно-религиозного просвещения, поскольку корни гуманитарного знания уходят вглубь истории, которая неразрывно связана с духовными ценностями в качестве основы жизнедеятельности цивилизаций. [1, с.63]</p>
                        
                        <p>Первая серия проекта посвящена одной из важнейших составляющих православной жизни &ndash; Таинства Евхаристии.</p>
                        
